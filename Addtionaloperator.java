class Addtionaloperator 
{
	public static void main(String[] args) 
	{
		double p1=100000;
		double p2=50000;
		System.out.println("initial amount of p1:"+p1);
		System.out.println("initial amount of p2:"+p2);
		double wallet=p1+p2;
		System.out.println("Total amount of p1 and p2:"+wallet);
	}
}
