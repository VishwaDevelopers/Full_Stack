import java.util.*;
class  EvenOrOdd1
{
	static Scanner sc=new Scanner(System.in);
	public static void main(String[] args) 
	{
		System.out.print("Enter the number:");
		int num=sc.nextInt();

		if (num%2==0)
		{
			System.out.println("Even");
		}
			else {
				System.out.println("Odd");
			}
		}
	}

